namespace Klipper\Bundle\ApiBundle\DependencyInjection\Compiler;

use Symfony\Component\DependencyInjection\Compiler\CompilerPassInterface;
use Symfony\Component\DependencyInjection\Compiler\PriorityTaggedServiceTrait;
use Symfony\Component\DependencyInjection\ContainerBuilder;

/**
 * @author François Pluchino <francois.pluchino@klipper.dev>
 */
class ViewTransformerPass implements CompilerPassInterface
{
    use PriorityTaggedServiceTrait;

    public function process(ContainerBuilder $container): void
    {
        if (!$container->hasDefinition('klipper_api.controller_handler')) {
            return;
        }

        $def = $container->getDefinition('klipper_api.controller_handler');

        foreach ($this->findAndSortTaggedServices('klipper_api.view_transformer', $container) as $service) {
            $def->addMethodCall('addViewTransformer', [$service, null, true]);
        }
    }
}
